#include "libft.h"

void	*ft_memset(void *s, int ch, size_t n)
{
	size_t	i;
	char	*t;

	i = 0;
	t = (char*)s;
	while (i < n)
	{
		t[i] = ch;
		i++;
	}
	return (t);
}
